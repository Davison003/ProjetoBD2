    public void clear() {
        cx_autor.setText("");
        cx_data.setText("");
        cx_descricao.setText("");
        cx_idaut.setText("");
        cx_idgen.setText("");
        cx_idliv.setText("");
        cx_isbn.setText("");
        cx_livro.setText("");
        cx_genero.setText("");
        cx_idaut.requestFocus();;
    }
    
    public void quit(){
        if (!cx_idaut.getText().isBlank() || !cx_idliv.getText().isBlank()){
            int op = JOptionPane.showConfirmDialog(null, "Deseja cancelar a insercao?", "Sair", 1);
            if(op == 0){
                clear();
                sair();
            }
        }
        else {
            sair();
        }
        
    }
    
    public void sair(){
        this.dispose();
    }
    
    
