    private void bt_confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_confirmActionPerformed
        // TODO add your handling code here:
        Autor aut = null;
        Livro liv = null;
        Genero gen = null;
        
        try {
            aut = new Autor(Integer.parseInt(cx_idaut.getText()), 
                            cx_autor.getText(),
                            "");
            
            liv = new Livro(Integer.parseInt(cx_idliv.getText()), cx_livro.getText(),
                    cx_descricao.getText(), java.sql.Date.valueOf(cx_data.getText()),
                    cx_isbn.getText(), Integer.parseInt(cx_idgen.getText()));
            
            gen = new Genero(Integer.parseInt(cx_idgen.getText()), cx_genero.getText(), "");
            
            App app = new App();
            app.setUser(Login.getFrameLogin().username);
            app.setPassword(Login.getFrameLogin().bdpassword);
            
            try {
                app.insertRelatLivros(aut, liv, gen);
                clear();
                RelatorioGeral.getRelatorioGeral().listarGeral();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Insert invalido", "Transacao invalida", 2);
            }
        }catch (IllegalArgumentException nfe) {
    		JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente!", "Transacao invalida", 2);
    	}
    }//GEN-LAST:event_bt_confirmActionPerformed

