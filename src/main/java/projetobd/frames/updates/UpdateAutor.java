    private void bt_confimrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_confimrActionPerformed
        // TODO add your handling code here:
        Autor aut = null;
        
        try {
            aut = new Autor(Integer.parseInt(cx_cod.getText()), 
                            cx_nome.getText(),
                            cx_bio.getText());
            
            App app = new App();
            app.setUser(Login.getFrameLogin().username);
            app.setPassword(Login.getFrameLogin().bdpassword);
            
            try {
                app.AltAutor(aut);
                clear();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Update invalido", "Transacao invalida", 2);
            }
        }catch (NumberFormatException nfe) {
    		JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente!", "Transacao invalida", 2);
    	}
    }//GEN-LAST:event_bt_confimrActionPerformed

    public void clear() {
        cx_cod.setText("");
        cx_nome.setText("");
        cx_bio.setText("");
        cx_cod.requestFocus();;
    }
    
    public void quit(){
        if (!cx_cod.getText().isBlank() || !cx_nome.getText().isBlank()){
            int op = JOptionPane.showConfirmDialog(null, "Deseja cancelar a insercao?", "Sair", 1);
            if(op == 0){
                clear();
                this.dispose();
            }
        }
        else {
            clear();
            sair();
        }
        
    }
    
    public void sair(){
        this.dispose();
    }
    
    
    
